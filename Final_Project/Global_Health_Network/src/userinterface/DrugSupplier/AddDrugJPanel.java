    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDrug.getModel();

        model.setRowCount(0);

        for (Drug d : business.getDrugList().getDrugList()) {

            Object[] row = new Object[2];
            row[0] = d.getDrugId();
            row[1] = d;
            model.addRow(row);

        }

    }

